<template>
  <AlertBox/>
  <form @submit="submitForm">
    <table class="table-borderless text-left">
      <tr>
        <th>Question Number:</th>
        <td>
          <input id="questionNumber" v-model="questionNumber" class="form-control" placeholder="Question Number"
                 required type="number"/>
        </td>
      </tr>
      <tr>
        <td colspan="6">&nbsp;</td>
      </tr>
      <tr>
        <th>Question Type:</th>
        <td>
          <select id="questionType" v-model="questionType" class="form-select form-select" required>
            <option v-for="qType in questionTypes" v-bind:key="qType.text" :value="qType.value">
              {{ qType.text }}
            </option>
          </select>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <th>Slug (Autogenerated):</th>
        <td>
          <p class="alert-secondary alert text-success">{{ getQuestionSlug }}</p>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <th>Question Title (English):</th>
        <td>
        <textarea id="questionTitleEnglish" v-model="questionTitleEnglish" class="form-control"
                  name="questionTitleEnglish"
                  rows="3" @keyup="createSlug" required></textarea>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <th>Question Title (Vietnamese):</th>
        <td>
        <textarea id="questionTitleVietnamese" v-model="questionTitleVietnamese" class="form-control"
                  name="questionTitleVietnamese"
                  rows="3"></textarea>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <th>Answers:</th>
        <td>
          <div class="card card-body bg-info">
            <div class="row">
              <div class="col-auto">
                <div v-for="(answer, index) in getAnswers" v-bind:key="index" class="row border-bottom border-1">
                  <div class="col">
                    <input :value="answer.answerValue"
                           class="form-control mt-1"
                           placeholder="Value (in English)" type="text" @keyup="updateAnswer($event, index, 'answerValue')" required/>

                  </div>

                  <div class="col mt-2">
                    <input :value="answer.answerTitleEnglish"
                           class="form-control mb-2"
                           placeholder="Text (English)" type="text" required @keyup="updateAnswer($event, index, 'answerTitleEnglish')"/>
                    <input :value="answer.answerTitleVietnamese"
                           class="form-control mb-2" placeholder="Text (Vietnamese)" type="text" @keyup="updateAnswer($event, index, 'answerTitleVietnamese')" />
                  </div>

                  <div v-if="index !== 0" class="col mt-2">
                    <button class="btn btn-danger material-icons text-white text-decoration-none"
                            @click="removeAnswer(index)">
                      remove
                    </button>
                  </div>

                </div>
              </div>

              <div class="col-auto">
                <button class="btn btn-success material-icons text-white text-decoration-none" @click="addNewAnswer" type="button">
                  add
                </button> &nbsp;

              </div>

            </div>
          </div>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <th>Additional Information (English):</th>
        <td>
        <textarea id="additionalInformationEnglish" v-model="additionalInformationEnglish" class="form-control"
                  name="additionalInformationEnglish"
                  rows="3"></textarea>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <th>Additional Information (Vietnamese):</th>
        <td>
        <textarea id="additionalInformationVietnamese" v-model="additionalInformationVietnamese" class="form-control"
                  name="additionalInformationVietnamese"
                  rows="3"></textarea>
        </td>
      </tr>
      <tr>
        <td>&nbsp;</td>
      </tr>
      <tr>
        <td>&nbsp;</td>
        <td>
          <button class="btn btn-success" type="submit">Save</button>&nbsp;&nbsp;
          <button class="btn btn-danger" @click="$router.push('/question')">Cancel</button>
        </td>
      </tr>
    </table>
  </form>

</template>

<script>
import {mapActions, mapGetters} from "vuex";
import AlertBox from "@/components/util/AlertBox";

export default {
  name: "QuestionForm",
  components: {
    AlertBox
  },
  methods: {
    ...mapActions([
        'fetchQuestionById',
      'createSlug',
      'addNewAnswer',
      'removeExistingAnswer',
      'saveQuestion',
      'updateAnswerFieldByIndex'
    ]),
    submitForm(event) {
      this.saveQuestion();
      event.preventDefault();
    },
    updateAnswer(event, answerIndex, fieldName) {
      this.updateAnswerFieldByIndex({index: answerIndex, fieldName: fieldName, value: event.currentTarget.value});
    },
    removeAnswer(index) {
      this.removeExistingAnswer(index)
    }
  },
  data() {
    return {
      questionTypes: [
        {
          text: "Select question type",
          value: "",
        },
        {
          text: "Scale",
          value: "scale",
        },
        {
          text: "Select",
          value: "select",
        },
      ]
    };
  },
  mounted() {
    this.$store.dispatch('hideAlert');
    if (this.$route.params.id) {
      this.fetchQuestionById(this.$route.params.id);
    }
  },
  computed: {
    ...mapGetters(['getQuestionSlug', 'getAnswers']),
    questionType: {
      get () {
        return this.$store.state.questionForm.questionType
      },
      set (value) {
        console.log("test ", value)
        this.$store.commit('UPDATE_QUESTION_TYPE', value)
      }
    },
    questionNumber: {
      get () {
        return this.$store.state.questionForm.questionNumber
      },
      set (value) {
        this.$store.commit('UPDATE_QUESTION_NUMBER', value)
      }
    },
    questionTitleEnglish: {
      get () {
        return this.$store.state.questionForm.questionTitleEnglish
      },
      set (value) {
        this.$store.commit('UPDATE_QUESTION_TITLE_ENGLISH', value)
      }
    },
    questionTitleVietnamese: {
      get () {
        return this.$store.state.questionForm.questionTitleVietnamese
      },
      set (value) {
        this.$store.commit('UPDATE_QUESTION_TITLE_VIETNAMESE', value)
      }
    },
    additionalInformationEnglish: {
      get () {
        return this.$store.state.questionForm.additionalInformationEnglish
      },
      set (value) {
        this.$store.commit('UPDATE_ADDITIONAL_INFORMATION_ENGLISH', value)
      }
    },
    additionalInformationVietnamese: {
      get () {
        return this.$store.state.questionForm.additionalInformationVietnamese
      },
      set (value) {
        this.$store.commit('UPDATE_ADDITIONAL_INFORMATION_VIETNAMESE', value)
      }
    }
  }
}
</script>

